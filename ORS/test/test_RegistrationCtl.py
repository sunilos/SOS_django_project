class TestRegistrationCtlSubmit(TestCase):
    """Tests for RegistrationCtl.submit() — verifies signup is called and success message set."""

    def setUp(self):
        self.ctl = RegistrationCtl()
        self.ctl.form.update({
            "id": "0", "firstName": "Jane", "lastName": "Doe",
            "login": "jane@example.com", "password": "pass",
            "dob": "", "mobileNumber": "9876543210", "gender": "Female", "role_id": 2,
        })
        self.request = MagicMock()

    @patch("ORS.ctl.RegistrationCtl.HtmlUtility")
    @patch("ORS.ctl.RegistrationCtl.render")
    @patch("ORS.ctl.RegistrationCtl.User")
    def test_submit_calls_signup(self, mock_user_cls, mock_render, mock_html):
        """submit() must call service.signup() with the constructed User instance."""
        mock_render.return_value = MagicMock()
        mock_html.get_list_from_list.return_value = ""
        user_instance = MagicMock()
        user_instance.id = 10
        mock_user_cls.return_value = user_instance
        mock_service = MagicMock()
        mock_service.signup.return_value = user_instance
        self.ctl.get_service = MagicMock(return_value=mock_service)

        self.ctl.submit(self.request, params={})

        mock_service.signup.assert_called_once()
        self.assertFalse(self.ctl.form["error"])
        self.assertIn("successful", self.ctl.form["message"])


